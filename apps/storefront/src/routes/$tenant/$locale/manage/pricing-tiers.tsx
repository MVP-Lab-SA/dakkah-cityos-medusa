// @ts-nocheck
import { createFileRoute } from "@tanstack/react-router"
import { useState, useCallback } from "react"
import { ManageLayout } from "@/components/manage"
import { Container, PageHeader, DataTable, SkeletonTable, Button, DropdownMenu, FormDrawer, ConfirmDialog, useToast } from "@/components/manage/ui"
import { t } from "@/lib/i18n"
import { useTenant } from "@/lib/context/tenant-context"
import { useQuery } from "@tanstack/react-query"
import { sdk } from "@/lib/utils/sdk"
import { useManageCrud } from "@/lib/hooks/use-manage-crud"
import { Plus } from "@medusajs/icons"

export const Route = createFileRoute("/$tenant/$locale/manage/pricing-tiers")({
  component: ManagePricingTiersPage,
})

const config = {
  moduleKey: "pricing-tiers",
  singularLabel: "Pricing Tier",
  pluralLabel: "Pricing Tiers",
  label: "Pricing Tiers",
  apiEndpoint: "/admin/pricing-tiers",
  fields: [
    { key: "name", label: "Name", type: "text" as const, required: true, placeholder: "Tier name" },
    { key: "min_quantity", label: "Min Quantity", type: "number" as const, required: true, placeholder: "1" },
    { key: "max_quantity", label: "Max Quantity", type: "number" as const, placeholder: "100" },
    { key: "discount_percentage", label: "Discount (%)", type: "number" as const, placeholder: "0" },
    { key: "product_type", label: "Product Type", type: "text" as const, placeholder: "All, Electronics, Clothing, etc." },
  ],
  defaultValues: { name: "", min_quantity: 1, max_quantity: 0, discount_percentage: 0, product_type: "" },
}

function ManagePricingTiersPage() {
  const { locale: routeLocale } = Route.useParams()
  const { locale: ctxLocale } = useTenant()
  const locale = routeLocale || ctxLocale || "en"
  const { addToast } = useToast()

  const [drawerOpen, setDrawerOpen] = useState(false)
  const [editingItem, setEditingItem] = useState<any>(null)
  const [formValues, setFormValues] = useState<Record<string, any>>(config.defaultValues)
  const [deleteId, setDeleteId] = useState<string | null>(null)

  const { data, isLoading } = useQuery({
    queryKey: ["manage", config.moduleKey],
    queryFn: async () => {
      const response = await sdk.client.fetch(config.apiEndpoint, { method: "GET" })
      return response
    },
    enabled: typeof window !== "undefined",
  })

  const { createMutation, updateMutation, deleteMutation } = useManageCrud({
    moduleKey: config.moduleKey,
    apiEndpoint: config.apiEndpoint,
  })

  const handleCreate = useCallback(() => {
    setEditingItem(null)
    setFormValues({ ...config.defaultValues })
    setDrawerOpen(true)
  }, [])

  const handleEdit = useCallback((row: any) => {
    setEditingItem(row)
    const values: Record<string, any> = {}
    config.fields.forEach((f) => { values[f.key] = row[f.key] ?? config.defaultValues[f.key] ?? "" })
    setFormValues(values)
    setDrawerOpen(true)
  }, [])

  const handleFormChange = useCallback((key: string, value: any) => {
    setFormValues((prev) => ({ ...prev, [key]: value }))
  }, [])

  const handleSubmit = useCallback(async () => {
    try {
      if (editingItem) {
        await updateMutation.mutateAsync({ id: editingItem.id, ...formValues })
        addToast("success", `${config.singularLabel} updated successfully`)
      } else {
        await createMutation.mutateAsync(formValues)
        addToast("success", `${config.singularLabel} created successfully`)
      }
      setDrawerOpen(false)
      setEditingItem(null)
    } catch (e) {
      addToast("error", `Failed to save ${config.singularLabel.toLowerCase()}`)
    }
  }, [editingItem, formValues, updateMutation, createMutation, addToast])

  const handleDelete = useCallback(async () => {
    if (!deleteId) return
    try {
      await deleteMutation.mutateAsync(deleteId)
      addToast("success", `${config.singularLabel} deleted successfully`)
      setDeleteId(null)
    } catch (e) {
      addToast("error", `Failed to delete ${config.singularLabel.toLowerCase()}`)
    }
  }, [deleteId, deleteMutation, addToast])

  const items = ((data as any)?.items || (data as any)?.pricing_tiers || []).map((item: any) => ({
    id: item.id,
    name: item.name || "—",
    min_quantity: item.min_quantity ?? "—",
    max_quantity: item.max_quantity ?? "—",
    discount_percentage: item.discount_percentage != null ? `${item.discount_percentage}%` : "—",
    product_type: item.product_type || "All",
  }))

  const columns = [
    {
      key: "name",
      header: t(locale, "manage.name"),
      render: (val: unknown) => <span className="font-medium">{val as string}</span>,
    },
    { key: "min_quantity", header: "Min Qty" },
    { key: "max_quantity", header: "Max Qty" },
    { key: "discount_percentage", header: "Discount" },
    { key: "product_type", header: "Product Type" },
    {
      key: "actions",
      header: t(locale, "manage.actions"),
      align: "end" as const,
      render: (_: unknown, row: any) => (
        <DropdownMenu
          items={[
            { label: t(locale, "common.actions.edit", "Edit"), onClick: () => handleEdit(row) },
            { type: "separator" as const },
            { label: t(locale, "common.actions.delete", "Delete"), onClick: () => setDeleteId(row.id), variant: "danger" as const },
          ]}
        />
      ),
    },
  ]

  if (isLoading) {
    return (
      <ManageLayout locale={locale}>
        <Container>
          <SkeletonTable rows={8} cols={6} />
        </Container>
      </ManageLayout>
    )
  }

  return (
    <ManageLayout locale={locale}>
      <Container>
        <PageHeader
          title={config.label}
          subtitle="Manage pricing tiers and volume discounts"
          actions={
            <Button variant="primary" size="base" onClick={handleCreate}>
              <Plus className="w-4 h-4" />
              Add {config.singularLabel}
            </Button>
          }
        />

        <DataTable columns={columns} data={items} emptyTitle="No pricing tiers found" countLabel="tiers" />
      </Container>

      <FormDrawer
        open={drawerOpen}
        onClose={() => { setDrawerOpen(false); setEditingItem(null) }}
        title={editingItem ? `Edit ${config.singularLabel}` : `Create ${config.singularLabel}`}
        fields={config.fields}
        values={formValues}
        onChange={handleFormChange}
        onSubmit={handleSubmit}
        loading={createMutation.isPending || updateMutation.isPending}
        submitLabel={editingItem ? t(locale, "common.actions.saveChanges", "Save changes") : t(locale, "common.actions.create", "Create")}
      />

      <ConfirmDialog
        open={!!deleteId}
        onClose={() => setDeleteId(null)}
        onConfirm={handleDelete}
        title={`Delete ${config.singularLabel}`}
        description={`Are you sure you want to delete this ${config.singularLabel.toLowerCase()}? This action cannot be undone.`}
        loading={deleteMutation.isPending}
      />
    </ManageLayout>
  )
}
