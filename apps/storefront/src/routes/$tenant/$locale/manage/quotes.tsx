import { createFileRoute } from "@tanstack/react-router"
import { useState } from "react"
import { ManageLayout } from "@/components/manage"
import { Container, PageHeader, DataTable, StatusBadge, SkeletonTable, Tabs, DropdownMenu } from "@/components/manage/ui"
import { t } from "@/lib/i18n"
import { useTenant } from "@/lib/context/tenant-context"
import { useQuery } from "@tanstack/react-query"
import { sdk } from "@/lib/utils/sdk"

export const Route = createFileRoute("/$tenant/$locale/manage/quotes")({
  component: ManageQuotesPage,
})

const STATUS_FILTERS = ["all", "draft", "pending", "accepted", "rejected", "expired"] as const

function ManageQuotesPage() {
  const { locale: routeLocale } = Route.useParams()
  const { locale: ctxLocale } = useTenant()
  const locale = routeLocale || ctxLocale || "en"
  const [statusFilter, setStatusFilter] = useState<string>("all")

  const { data, isLoading } = useQuery({
    queryKey: ["manage", "quotes"],
    queryFn: async () => {
      const response = await sdk.client.fetch("/admin/quotes", { method: "GET" })
      return response
    },
    enabled: typeof window !== "undefined",
  })

  const allQuotes = ((data as any)?.quotes || []).map((q: any) => ({
    id: q.id,
    display_id: q.id?.slice(0, 8) || "—",
    customer: q.customer?.first_name
      ? `${q.customer.first_name} ${q.customer.last_name || ""}`.trim()
      : q.customer_email || "—",
    total: q.total ? `$${(q.total / 100).toFixed(2)}` : "$0.00",
    status: q.status || "draft",
    expires_at: q.expires_at ? new Date(q.expires_at).toLocaleDateString() : "—",
  }))

  const quotes = statusFilter === "all"
    ? allQuotes
    : allQuotes.filter((q: any) => q.status === statusFilter)

  const columns = [
    {
      key: "display_id",
      header: t(locale, "manage.quote_id"),
      render: (val: unknown) => <span className="font-medium font-mono">{val as string}</span>,
    },
    {
      key: "customer",
      header: t(locale, "manage.customer"),
    },
    {
      key: "total",
      header: t(locale, "manage.total"),
      align: "end" as const,
    },
    {
      key: "status",
      header: t(locale, "manage.status"),
      render: (val: unknown) => <StatusBadge status={val as string} />,
    },
    {
      key: "expires_at",
      header: t(locale, "manage.expires_at"),
    },
    {
      key: "actions",
      header: t(locale, "manage.actions"),
      align: "end" as const,
      render: () => (
        <DropdownMenu
          items={[
            { label: t(locale, "manage.view"), onClick: () => {} },
          ]}
        />
      ),
    },
  ]

  if (isLoading) {
    return (
      <ManageLayout locale={locale}>
        <Container>
          <SkeletonTable rows={8} cols={6} />
        </Container>
      </ManageLayout>
    )
  }

  return (
    <ManageLayout locale={locale}>
      <Container>
        <PageHeader
          title={t(locale, "manage.quotes")}
          subtitle={t(locale, "manage.manage_quotes")}
        />

        <Tabs
          tabs={STATUS_FILTERS.map((s) => ({
            id: s,
            label: s === "all" ? t(locale, "manage.all_statuses") : s.replace(/_/g, " "),
          }))}
          activeTab={statusFilter}
          onTabChange={setStatusFilter}
          className="mb-4"
        />

        <DataTable
          columns={columns}
          data={quotes}
          searchable
          searchPlaceholder={t(locale, "manage.search_by_id")}
          searchKey="display_id"
          emptyTitle={t(locale, "manage.no_quotes")}
          countLabel={t(locale, "manage.quotes").toLowerCase()}
        />
      </Container>
    </ManageLayout>
  )
}
