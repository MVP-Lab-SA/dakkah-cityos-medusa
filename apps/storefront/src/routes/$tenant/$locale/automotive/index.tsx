// @ts-nocheck
import { getServerBaseUrl, fetchWithTimeout } from "@/lib/utils/env"
import { createFileRoute, Link } from "@tanstack/react-router"
import { useState } from "react"
import { t } from "@/lib/i18n"

export const Route = createFileRoute("/$tenant/$locale/automotive/")({
  component: AutomotivePage,
  head: () => ({
    meta: [
      { title: "Automotive | Dakkah CityOS" },
      { name: "description", content: "Browse automotive listings on Dakkah CityOS" },
    ],
  }),
  loader: async () => {
    try {
      const baseUrl = getServerBaseUrl()
      const resp = await fetchWithTimeout(`${baseUrl}/store/automotive`, {
        headers: {
          "x-publishable-api-key": import.meta.env.VITE_MEDUSA_PUBLISHABLE_KEY || "pk_56377e90449a39fc4585675802137b09577cd6e17f339eba6dc923eaf22e3445",
        },
      })
      if (!resp.ok) return { items: [], count: 0 }
      const data = await resp.json()
      const raw = data.items || data.listings || data.products || []
      const items = raw.map((s: any) => {
        const meta = s.metadata || {}
        return {
          id: s.id,
          title: s.title || `${s.year || ""} ${s.make || ""} ${s.model_name || ""}`.trim() || "Vehicle Listing",
          make: s.make || null,
          model_name: s.model_name || null,
          year: s.year || null,
          mileage_km: s.mileage_km || null,
          fuel_type: s.fuel_type || null,
          transmission: s.transmission || null,
          body_type: s.body_type || null,
          listing_type: s.listing_type || null,
          thumbnail: meta.thumbnail || meta.images?.[0] || null,
          images: meta.images || [],
          price: meta.price || null,
          currency: meta.currency || "SAR",
          location: meta.location || null,
        }
      })
      return { items, count: data.count || items.length }
    } catch {
      return { items: [], count: 0 }
    }
  },
})

